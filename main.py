class Animal:
  pass

# летающие 
class Flying:
  pass

# Корова
class Cow(Animal):
  pass

# коза
class Goat(Animal):
  pass

# свинья
class pig(Animal):
  pass

# Овца
class Sheep(Animal):
  pass

# курица
class Hen(Animal):
  pass

# утка
class Duck(Animal, Flying):
  pass

# гусь
class Goose(Animal, Flying):
  pass
 